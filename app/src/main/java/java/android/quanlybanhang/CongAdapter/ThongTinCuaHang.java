package java.android.quanlybanhang.CongAdapter;

public class ThongTinCuaHang {
    String chuyenKhoan;
    String ghiChu;
    String logoUrl;
    String moTa;
    String name;
    String phuongXa;
    String quanHuyen;
    String soDienThoai;
    String soNha;
    String thongTinChuyenKhoan;
    String tienMat;
    String tinhThanhPho;

    public ThongTinCuaHang() {
    }

    public ThongTinCuaHang(String chuyenKhoan, String ghiChu, String logoUrl, String moTa, String name, String phuongXa, String quanHuyen, String soDienThoai, String soNha, String thongTinChuyenKhoan, String tienMat, String tinhThanhPho) {
        this.chuyenKhoan = chuyenKhoan;
        this.ghiChu = ghiChu;
        this.logoUrl = logoUrl;
        this.moTa = moTa;
        this.name = name;
        this.phuongXa = phuongXa;
        this.quanHuyen = quanHuyen;
        this.soDienThoai = soDienThoai;
        this.soNha = soNha;
        this.thongTinChuyenKhoan = thongTinChuyenKhoan;
        this.tienMat = tienMat;
        this.tinhThanhPho = tinhThanhPho;
    }

    public String getChuyenKhoan() {
        return chuyenKhoan;
    }

    public void setChuyenKhoan(String chuyenKhoan) {
        this.chuyenKhoan = chuyenKhoan;
    }

    public String getGhiChu() {
        return ghiChu;
    }

    public void setGhiChu(String ghiChu) {
        this.ghiChu = ghiChu;
    }

    public String getLogoUrl() {
        return logoUrl;
    }

    public void setLogoUrl(String logoUrl) {
        this.logoUrl = logoUrl;
    }

    public String getMoTa() {
        return moTa;
    }

    public void setMoTa(String moTa) {
        this.moTa = moTa;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhuongXa() {
        return phuongXa;
    }

    public void setPhuongXa(String phuongXa) {
        this.phuongXa = phuongXa;
    }

    public String getQuanHuyen() {
        return quanHuyen;
    }

    public void setQuanHuyen(String quanHuyen) {
        this.quanHuyen = quanHuyen;
    }

    public String getSoDienThoai() {
        return soDienThoai;
    }

    public void setSoDienThoai(String soDienThoai) {
        this.soDienThoai = soDienThoai;
    }

    public String getSoNha() {
        return soNha;
    }

    public void setSoNha(String soNha) {
        this.soNha = soNha;
    }

    public String getThongTinChuyenKhoan() {
        return thongTinChuyenKhoan;
    }

    public void setThongTinChuyenKhoan(String thongTinChuyenKhoan) {
        this.thongTinChuyenKhoan = thongTinChuyenKhoan;
    }

    public String getTienMat() {
        return tienMat;
    }

    public void setTienMat(String tienMat) {
        this.tienMat = tienMat;
    }

    public String getTinhThanhPho() {
        return tinhThanhPho;
    }

    public void setTinhThanhPho(String tinhThanhPho) {
        this.tinhThanhPho = tinhThanhPho;
    }
}
